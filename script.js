const topicSelect = document.getElementById("topic-select");

const courseSelect = document.getElementById("course-select");

const startBtn = document.getElementById("start-btn");

const quizContainer = document.querySelector(".quiz-container");

const questionEl = document.getElementById("question");

const optionsEl = document.getElementById("options");

const nextBtn = document.getElementById("next-btn");



let currentTopic = "";

let currentCourse = "";

let questions = [];

let currentIndex = 0;

let score = 0;     





topicSelect.addEventListener("change", () => {

  currentTopic = topicSelect.value;

  courseSelect.innerHTML = '<option value="">-- Select a course --</option>';



  if (quizData[currentTopic]) {

    courseSelect.disabled = false;

    const courses = Object.keys(quizData[currentTopic]);

    courses.forEach(course => {

      const opt = document.createElement("option");

      opt.value = course;

      opt.textContent = course;

      courseSelect.appendChild(opt);

    });

  } else {

    courseSelect.disabled = true;

  }

});



startBtn.addEventListener("click", () => {

  currentTopic = topicSelect.value;

  currentCourse = courseSelect.value;



  if (!quizData[currentTopic] || !quizData[currentTopic][currentCourse]) {

    alert("Please select a valid topic and course.");

    return;

  }



  questions = [...quizData[currentTopic][currentCourse]];

  shuffleArray(questions);

  currentIndex = 0;

  score = 0;



  document.querySelector(".topic-select").style.display = "none";

  quizContainer.style.display = "block";



  loadQuestion();

});

function loadQuestion() {
  const currentQ = questions[currentIndex];
  questionEl.textContent = currentQ.question;
  optionsEl.innerHTML = "";
  nextBtn.disabled = true;

  currentQ.options.forEach(option => {
    const btn = document.createElement("button");
    btn.textContent = option;
    btn.addEventListener("click", () => selectAnswer(btn, currentQ.answer));
    optionsEl.appendChild(btn);
  });
}

function selectAnswer(button, correctAnswer) {
  const buttons = optionsEl.querySelectorAll("button");
  buttons.forEach(btn => {
    btn.disabled = true;
    if (btn.textContent === correctAnswer) {
      btn.style.backgroundColor = "lightgreen";
    } else {
      btn.style.backgroundColor = "lightcoral";
    }
  });

  if (button.textContent === correctAnswer) {
    score++;
  }

  nextBtn.disabled = false;
}

// Show saved scores
function displaySavedScores() {
  const container = document.getElementById("previous-score");
  let output = `<h3>📘 Your Stats:</h3><ul>`;

  for (let i = 0; i < localStorage.length; i++) {
    const topic = localStorage.key(i);
    const result = localStorage.getItem(topic);
    output += `<li><strong>${topic}:</strong> ${result}</li>`;
  }

  output += `</ul>`;
  container.innerHTML = output;
}

